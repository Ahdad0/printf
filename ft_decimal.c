#include "ft_printf.h"

int	ft_decimal(int d)
{
	int		j;

	j = 0;
	char *a = ft_itoa(d);
	if (!a)
	{
		free(a);
		return (-1);
	}
	j += ft_putstr(a);
	free(a);
	return (j);
}
