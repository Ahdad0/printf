#include "ft_printf.h"

int	ft_decimal(int d)
{
	int		j;
	char	*a;

	j = 0;
	if (d == -2147483648)
		return (write(1, "-2147483648", 11));
	a = ft_itoa(d);
	if (!a)
	{
		free(a);
		return (-1);
	}
	j += ft_putstr(a);
	free(a);
	return (j);
}
